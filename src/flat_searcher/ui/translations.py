_RESOURCE_TEXT_RU: dict[str, str] = {
    text_id: _RUSSIAN.get(english, english) for text_id, english in _RESOURCE_TEXT_EN.items()
}
_RESOURCE_TEXT_RU.update(_RESOURCE_RU_OVERRIDES)

_RESOURCE_TABLES: dict[str, dict[str, str]] = {
    "en": _RESOURCE_TEXT_EN,
    "ru": _RESOURCE_TEXT_RU,
}
_LEGACY_TEXT_IDS: dict[str, str] = {
    english: text_id for text_id, english in _RESOURCE_TEXT_EN.items()
}


def ui_strings(language: str) -> dict[str, str]:
    table = _RESOURCE_TABLES.get(language, _RESOURCE_TEXT_EN)
    strings = {
        text_id: table.get(text_id, english) for text_id, english in _RESOURCE_TEXT_EN.items()
    }
    strings.update({english: strings[text_id] for english, text_id in _LEGACY_TEXT_IDS.items()})
    return strings


def translate(language: str, text: str) -> str:
    table = _RESOURCE_TABLES.get(language, _RESOURCE_TEXT_EN)
    if text in _RESOURCE_TEXT_EN:
        return table.get(text, _RESOURCE_TEXT_EN[text])
    text_id = _LEGACY_TEXT_IDS.get(text)
    if text_id is not None:
        return table.get(text_id, _RESOURCE_TEXT_EN[text_id])
    return _RUSSIAN.get(text, text) if language == "ru" else text
